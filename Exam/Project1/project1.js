// List variable nilai
const nilai1 = 85;
const nilai2 = 80;
const nilai3 = 95;
const nilai4 = 60;
const nilai5 = 70;

// Mean of nilai
console.log(`Rata-rata nilai kelas: ${(nilai1 + nilai2 + nilai3 + nilai4 + nilai5) / 5}`);
// Max of nilai
console.log(`Nilai tertinggi: ${Math.max(nilai1, nilai2, nilai3, nilai4, nilai5)}`);
// Min of nilai
console.log(`Nilai tertinggi: ${Math.min(nilai1, nilai2, nilai3, nilai4, nilai5)}`);